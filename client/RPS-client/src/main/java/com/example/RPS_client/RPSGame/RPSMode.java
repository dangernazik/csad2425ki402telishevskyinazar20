package com.example.RPS_client.RPSGame;

/**
 * @brief Enumeration representing the different game modes for Rock, Paper, Scissors.
 * @details This enum defines the available modes:
 *          - MAN_VS_MAN: Player 1 vs Player 2 (Human vs Human).
 *          - MAN_VS_AI: Player 1 (Human) vs Player 2 (AI).
 *          - AI_VS_AI: Player 1 (AI) vs Player 2 (AI).
 */
public enum RPSMode {
    MAN_VS_MAN,
    MAN_VS_AI,
    AI_VS_AI
}
